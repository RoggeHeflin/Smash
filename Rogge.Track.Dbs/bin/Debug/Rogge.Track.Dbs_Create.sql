				[SchemaName]	= [s].[name],
				[ObjectName]	= [p].[name],
				[SqlDef]		= OBJECT_DEFINITION([p].[object_id])
		FROM
			sys.schemas						[s]
		INNER JOIN
			sys.procedures					[p]
				ON	([s].[schema_id]	=	[p].[schema_id])
		) [t]
