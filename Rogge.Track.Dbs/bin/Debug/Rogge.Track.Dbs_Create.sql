CREATE FUNCTION [track].[ProcedureAttributes]
(
	@ExecuteThreshold	INT	=	3
)
RETURNS TABLE
AS
RETURN
(
	SELECT
		[t].[object_id],

		[t].[SchemaName],
		[t].[ObjectName],
			[QualifiedName]	= QUOTENAME([t].[SchemaName]) + N'.' + QUOTENAME([t].[ObjectName]),

